;;; Integer literals
writeln(1);
writeln(+1);
writeln(-1);
writeln(12345678901234567890);

;;; Floating point literals
writeln(1.0);
writeln(+1.0);
writeln(-1.0);
writeln(1.0e2);
writeln(1.0e+2);
writeln(1.0e-2);

;;; String literals
writeln("hello there");
writeln("general\n\tkenobi");
writeln("\\\"\'");

;;; Character literals
writeln('a');

;;; Boolean literals
writeln(true);
writeln(false);

;;; Null reference
writeln(null);
